#{} fait un calcul et dit false or true si l'interieur est une comparaison faux ou vrai 
puts "On va compter le nombre d'heures de travail à THP" #affichage du texte
puts "Tavail : #{10*5*11}" #imprine travail et calcule 10*5*11=550
puts "En minutes ça fait : #{10*5*11*60}" #imprime en minutes ça fait et calcule 10*5*11*60=33000
puts "Et en secondes?" # affichage et à la ligne
puts 10*5*11*60*60 #operation 
puts "Est-ce que c'est vrai que 3+2<5-7?" # affichage d'une question
puts 3+2<5-7 #repondre la question précédente par une comparaison fausse poour avoir false
puts "Ça fait combien 3 + 2 ? #{3 + 2}" # affiche les textes et calcul 3+2
